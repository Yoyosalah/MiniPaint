    public static boolean isDouble(String str) {
        try {
            Double.parseDouble(str);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
    
    private void clearInputs(){
        inputWidth.setText("");
        inputHeight.setText("");
        inputCoorX.setText("");
        inputCoorY.setText("");
    }
    
