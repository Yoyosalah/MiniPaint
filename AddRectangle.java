    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String width = inputWidth.getText();
        String height = inputHeight.getText();
        String coorX = inputCoorX.getText();
        String coorY = inputCoorY.getText();

        if(width.isEmpty() || height.isEmpty() || coorX.isEmpty() || coorY.isEmpty()){
            JOptionPane.showMessageDialog(this, "Some fields are empty!", "Error", JOptionPane.ERROR_MESSAGE);
        } else if(!isDouble(height) || !isDouble(width) || !isDouble(coorX) || !isDouble(coorY)){
            JOptionPane.showMessageDialog(this, "Input Fields must be numbers!", "Error", JOptionPane.ERROR_MESSAGE);
            clearInputs();
        } else{
            Point p = new Point((int)Double.parseDouble(coorX),(int)Double.parseDouble(coorY));
            Rectangle rectangle = new Rectangle(Double.parseDouble(width),Double.parseDouble(height),p);
            prev.addShape(rectangle);
            rectangle.draw(canvas);
            String name = "rectangle "+(++prev.rectangleCounter);
            prev.addToBox(name);
            prev.addToMap(name,rectangle);
            this.setVisible(false);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

